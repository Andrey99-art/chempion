// src-tauri/src/db/migrations.rs
//
// Версионирование схемы базы данных.
// При каждом обновлении приложения проверяем текущую версию БД
// и применяем нужные миграции последовательно.
// Данные клиентов никогда не теряются — только добавляются новые столбцы/таблицы.

use rusqlite::Connection;

/// Текущая версия схемы базы данных.
/// При добавлении новой миграции — увеличиваем на 1.
const CURRENT_VERSION: i32 = 2;

/// Проверяет версию БД и применяет все необходимые миграции
pub fn run_migrations(conn: &Connection) -> Result<(), rusqlite::Error> {
    // Узнаём текущую версию БД
    // user_version — встроенная переменная SQLite, хранится в самом файле .db
    let current: i32 = conn.query_row(
        "PRAGMA user_version",
        [],
        |row| row.get(0),
    )?;

    // Если версия совпадает с текущей — миграции не нужны
    if current >= CURRENT_VERSION {
        return Ok(());
    }

    // Применяем миграции последовательно, начиная с текущей версии + 1
    // Каждая миграция обёрнута в условие: if current < N
    // Это гарантирует, что каждая миграция выполнится ровно один раз

    if current < 1 {
        // Миграция v0 → v1: начальная версия
        // Таблицы уже созданы в schema.rs через CREATE TABLE IF NOT EXISTS,
        // поэтому здесь ничего дополнительного не требуется.
        // Эта секция — заглушка для будущих миграций.
        //
        // Пример будущей миграции (v1 → v2):
        // if current < 2 {
        //     conn.execute_batch(
        //         "ALTER TABLE clients ADD COLUMN email TEXT;"
        //     )?;
        // }
    }

    if current < 2 {
        // Миграция v1 → v2: таблицы тренировок
        // Таблицы создаются в schema.rs через CREATE TABLE IF NOT EXISTS
    }

    // Обновляем версию БД до текущей
    // PRAGMA не поддерживает параметры (?), поэтому используем format!
    conn.execute_batch(
        &format!("PRAGMA user_version = {};", CURRENT_VERSION)
    )?;

    Ok(())
}